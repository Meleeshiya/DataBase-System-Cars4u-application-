 END_CENTREID NUMBER(4) REFERENCES HIRECENTRE (CENTREID) on delete CASCADE,
 TRANSACTIONID NUMBER (4) REFERENCES TRANSACTIONX (TRANSACTIONID),
 CONSTRAINT PK_HIREARRANGEMENT PRIMARY KEY (HIREARRANGEMENTID));
 
 CREATE TABLE VEHICLECLASSIFICATION ( 
 VEHICLECLASSIFICATIONID NUMBER (4),
 VC_NAME VARCHAR2(30),
 NUMBEROF_PASSENGERS NUMBER(2),
 CONSTRAINT PK_VEHICLECLASSIFICATION PRIMARY KEY (VEHICLECLASSIFICATIONID));
 
 CREATE TABLE MODELX( 
 MODELID NUMBER (4) ,
 MODEL_NAME VARCHAR2(30),
 VEHICLECLASSIFICATIONID NUMBER(4) REFERENCES VEHICLECLASSIFICATION(VEHICLECLASSIFICATIONID),
 CONSTRAINT PK_MODEL PRIMARY KEY (MODELID));
 
CREATE TABLE VEHICLE( 
 VEHICLEID NUMBER (4) ,
 V_COLOR VARCHAR2(15),
 V_REGISTRATIONNO VARCHAR2(15) NOT NULL,
 V_MAKE VARCHAR2(15),
 VEHICLECLASSIFICATIONID NUMBER(4) REFERENCES VEHICLECLASSIFICATION(VEHICLECLASSIFICATIONID),
 MODELID NUMBER(4) REFERENCES MODELX(MODELID),
 CONSTRAINT PK_VEHICLE PRIMARY KEY (VEHICLEID));
 
