CREATE TABLE CUSTOMER ( 
 CUSTOMERID NUMBER (4), 
 CNAME VARCHAR2 (40) NOT NULL, 
 C_TYPE VARCHAR2(20),
 C_JOINDATE DATE NOT NULL,
 C_ADD1 VARCHAR2 (50),
 C_ADD2 VARCHAR2 (50),
 C_ADD3 VARCHAR2 (50),
 C_TOWN VARCHAR2 (15),
 C_COUNTRY VARCHAR2 (15),
 C_POSTCODE VARCHAR (10),
 C_EMAIL VARCHAR2 (30) UNIQUE,
 C_CONTACTNO NUMBER(15),
 IC_DOB DATE,
 IC_TITLE VARCHAR2(5),
 IC_FORENAMES VARCHAR2(20),
 IC_SURNAMES VARCHAR2(20),
 IC_NINUMBER VARCHAR2(12),
 IC_CONTACTNAME VARCHAR2(20),
 IC_FAX VARCHAR2(15),
 CONSTRAINT PK_CUSTOMER PRIMARY KEY (CUSTOMERID));
 
 CREATE TABLE TRANSACTIONX ( 
 TRANSACTIONID NUMBER (4) ,
 T_TS TIMESTAMP NOT NULL,
 T_AMOUNT FLOAT(126),
 T_STATUS VARCHAR2(16),
 CUSTOMERID NUMBER (4) REFERENCES CUSTOMER (CUSTOMERID) on delete CASCADE, 
 CONSTRAINT PK_TRANSACTIONX PRIMARY KEY (TRANSACTIONID));
 
CREATE TABLE COUNTRYFREQUSED ( 
