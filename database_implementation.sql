 COUNTRY_NAME VARCHAR2(30) NOT NULL,
 CONSTRAINT PK_COUNTRYFREQUSED PRIMARY KEY (COUNTRYCODE));
CREATE TABLE COUNTRYINFO ( 
 COUNTRYCODE NUMBER (4) ,
 COUNTRY_CONTINENTNAME VARCHAR2(30),
 CONSTRAINT PK_COUNTRYINFO PRIMARY KEY (COUNTRYCODE));
 
CREATE TABLE HIRECENTRE ( 
 CENTREID NUMBER (4),
 CENTRE_NAME VARCHAR2(50),
 CENTRE_ADDRESS VARCHAR2 (50),
 CENTRE_POSTCODE VARCHAR2(30) NOT NULL,
 COUNTRYCODE NUMBER (4) REFERENCES COUNTRYFREQUSED (COUNTRYCODE) on delete CASCADE, 
 CONSTRAINT PK_HIRECENTRE PRIMARY KEY (CENTREID));
 
CREATE TABLE HIREARRANGEMENT ( 
